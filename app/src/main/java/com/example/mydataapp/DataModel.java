package com.example.mydataapp;

public class DataModel {

    String nim, nama, prodi, kelas, alamat, email;

    public DataModel(String nim, String nama, String prodi, String kelas, String alamat, String email) {
        this.nim = nim;
        this.nama = nama;
        this.prodi = prodi;
        this.kelas = kelas;
        this.alamat = alamat;
        this.email = email;
    }
}